// Define the numbers and the operation here
let num1=5;
let num2=3;
let operation="*";

// Write if-statements here

if(operation === "+"){
    console.log(`Addtion is: ${num1+num2}`);
}
else if(operation === "-"){
    console.log(`Subtract is: ${num1-num2}`);
}
else if(operation === "*"){
    console.log(`Multiplication is: ${num1*num2}`);
}
else if(operation === "/"){
    console.log(`Divition is: ${num1/num2}`);
}